// Variáveis em C
#include <stdio.h>

int main(void)
{
	// Declaração de variável do tipo inteiro
	int x;
	int y;
	int soma;
	// Inicialização de variável
	x = 5;
	y = 2;
	soma = x + y;

	// Exibe o resultado da soma de x e y que estão armazenados na variável soma
	printf("%d\n", soma);

	return 0;
}
