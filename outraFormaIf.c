// Outra forma de utilizar a estrutura if é utilizando em uma única linha
#include <stdio.h>
#include <stdbool.h>

int main(void)
{
	/* Exemplo de como utilizar a estrutura if em uma linha(single line), para isso utilizamos a condição seguido do comando. */
	if(true) printf("True\n");

	return 0;
}
